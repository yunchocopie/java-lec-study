package ex02;

public class comOperator {
    public static void main(String[] args) {
        System.out.print((3 == 4) + " ");
        System.out.print((3 != 4) + " ");
        System.out.print((3 > 4) + " ");
        System.out.print((4 > 3) + " ");

        System.out.print((3 == 3 && 4 == 7) + " ");
        System.out.print((3 == 3 || 4 == 7) + " ");


    }
}
